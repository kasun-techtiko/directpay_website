                    <h2>DirectPay wins national “Best Disruptor” award from SLASSCOM</h2>
                    <div class="row mt20 mb20">
                        <div class="col-md-8 col-9">
                            <div class="media">
                                <div class="user-image bdr-radius"><img src="images/user-thumb/paymedia.jpg" alt="girl"
