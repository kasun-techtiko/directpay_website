<p>The privacy policy will allow you to understand the types of personal information we collect, why we collect your personal information, and the measures we take to protect your personal information. Our privacy practices reflect the local legal framework and the Data Protection Act 2018 accordingly.</p><br/>

<h4>Your Privacy is Safe with Us</h4><br/>

<p>At DirectPay, we respect our customers’ trust with their privacy. In order to ensure the confidentiality of your personal information, we uphold the the following principles,</p><br/>

<p><strong>**We do not store any credit/debit card or Bank account details with us. These information are tokenized and stored within the bank itself. </strong></p><br/>

<h4>Personal Information We May Collect</h4><br/>

<p>When conducting business with our customers, merchants and other clients, we collect updated relevant personal information relating to a specific individual. Examples of such personal information would include: name, email address, EMEI number, phone number, date of birth and business address. We may also collect;</p><br/>

<ul class="list-style-">
<li>Transaction information, such as the merchant’s name and location, the date and time, total amount of the transaction, and relevant information provided to us by the financial institutions or merchants when we act on their behalf.</li>
<li>Product and service information, such as registration and payment information, and program-specific information, when you request products or services directly from us, or participate in marketing programs.</li>
<li>Website, device and mobile app usage and similar information collected via automated means, such as cookies and similar technologies.</li>
<li>Job applications and related information when you apply for a job with us.</li>
<li>Business contact information when you work for one of our business partners.</li><br/>
</ul>
<h4>How We May Collect Your Personal Information</h4><br/>
<p>We may collect your personal information;</p><br/>
<ul class="list-style-">
<li>Directly from you</li>
<li>Through our marketing campaigns and promotions</li>
<li>Over the Telephone and Email</li>
<li>Through the DirectPay App or DirectPay Agents</li>
<li>Referees</li>
<li>Via Website Usage</li><br/>
</ul>
<h4>How We May Use Your Personal Information?</h4><br/>
<p>We will use your personal information;</p>
<ul class="list-style-">
<li>To process your application for account-based services or facilities.</li>
<li>To establish and manage your account or facility and carry out transactions you wish to make.</li>
<li>To provide requested services to you, and bill you for our services and collect any payments outstanding.</li>
<li>Protect against and prevent fraud, and other legal or information security risks.</li>
<li>Provide and communicate with you about products and services offered by DirectPay, financial institutions, merchants and partners.</li>
<li>Provide you with personalized services and recommendations.</li>
<li>Process your job application.</li>
<li>Serve other purposes for which we provide specific notice at the time of collection, and as otherwise authorized or required by law.</li>
</ul><br/>
<h4>How We Protect Your Personal Information</h4><br/>
<p>We maintain appropriate security safeguards to protect your Personal Information and only retain it for the required period of time. Safety processes such as secure computer storage facility, authorized access and other modes are used to safeguard your personal information from misuse, disclosure, modification and unauthorised access.We use secure technology and state of the art security systems to protect your personal information when it is transmitted safely to us through electronic and digital means.</p>
<p>All required processes will be taken into complete deletion of your personal information when it is no longer required or as per your request.</p>
<h4>How We May Share Your Personal Information</h4><br/>
<p>We may share your personal information for the given purposes below;</p>
<ul class="list-style-">
<li>To store within the DirectPay server.</li>
<li>Where we have contracted an external organization to provide us with support services. This may include but is not limited to processing application or orders, marketing support, deliveries, market research and debt collection. These service providers may be within or outside Sri Lanka.</li>
<li>To comply with our legal and regulatory obligations.</li>
<li>Where we suspect that unlawful activity has been or may be engaged in and the personal information is a necessary part of our investigation or reporting of the matter to necessary authorities. We also may disclose personal information about you in the following circumstances. Where you have a corporate or government card membership, we may exchange personal information with your employer or government agency.</li>
<li>To our related companies to provide you with products and services you request and to inform you about other products and services.</li>
<li>To anyone else you authorise us to disclose information to, from time to time.</li>
<li>We reserve the right to amend our prevailing data protection and privacy policy at any time and will place any such amendments on this Website.</li>
</ul><br/>
